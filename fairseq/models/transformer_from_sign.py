class GTConv(nn.Module):
    
    def __init__(self, in_channels, out_channels):
        super(GTConv, self).__init__()
        self.in_channels = in_channels
        self.out_channels = out_channels
        self.weight = nn.Parameter(torch.Tensor(out_channels,in_channels,1,1))
        self.bias = None
        self.scale = nn.Parameter(torch.Tensor([0.1]), requires_grad=False)
        self.reset_parameters()

    def reset_parameters(self):
        n = self.in_channels
        nn.init.constant_(self.weight, 0.1)
        if self.bias is not None:
            fan_in, _ = nn.init._calculate_fan_in_and_fan_out(self.weight)
            bound = 1 / math.sqrt(fan_in)
            nn.init.uniform_(self.bias, -bound, bound)

    def forward(self, A):
        A = torch.sum(A*F.softmax(self.weight, dim=1), dim=1)
        return A


class GTLayer(nn.Module):
    
    def __init__(self, in_channels, out_channels, first=True):
        super(GTLayer, self).__init__()
        self.in_channels = in_channels
        self.out_channels = out_channels
        self.first = first
        if self.first == True:
            self.conv1 = GTConv(in_channels, out_channels)
            self.conv2 = GTConv(in_channels, out_channels)
        else:
            self.conv1 = GTConv(in_channels, out_channels)
    
    def forward(self, A, H_=None):
        if self.first == True:
            a = self.conv1(A)
            b = self.conv2(A)
            H = torch.bmm(a,b)
            W = [(F.softmax(self.conv1.weight, dim=1)).detach(),(F.softmax(self.conv2.weight, dim=1)).detach()]
        else:
            a = self.conv1(A)
            H = torch.bmm(H_,a)
            W = [(F.softmax(self.conv1.weight, dim=1)).detach()]
        return H,W


class GTEncoder(nn.Module):

    def __init__(self, num_matrices, num_layers, num_channels=1):
        super(GTEncoder, self).__init__()
        self.num_layers = num_layers
        self.num_channels = num_channels
        layers = []
        for i in range(num_layers):
            # notice original has typo here for the 1st parameter into GTLayer,
            # for it is learning to combine multiple matrices and thus the in_channel is the number
            # of matrices, not edges
            if i == 0:
                layers.append(GTLayer(num_matrices, num_channels, first=True))
            else:
                layers.append(GTLayer(num_matrices, num_channels, first=False))    
        self.layers = nn.ModuleList(layers)

        self.eye_matrix = nn.Parameter(torch.eye(4096), requires_grad=False)

    def norm(self, H, add=False):
        H = H.t()
        eye_matrix = self.eye_matrix[:H.shape[0], :H.shape[0]]
        if add == False:
            # H = H*((torch.eye(H.shape[0])==0).type(torch.FloatTensor))
            H = H*((eye_matrix==0).type(torch.float))
        else:
            # H = H*((torch.eye(H.shape[0])==0).type(torch.FloatTensor)) + torch.eye(H.shape[0]).type(torch.FloatTensor)
            H = H*((eye_matrix==0).type(torch.float)) + eye_matrix.type(torch.float)
        deg = torch.sum(H, dim=1)
        deg_inv = deg.pow(-1)
        deg_inv[deg_inv == float('inf')] = 0
        # deg_inv = deg_inv*torch.eye(H.shape[0]).type(torch.FloatTensor)
        deg_inv = deg_inv*eye_matrix.type(torch.float)
        H = torch.mm(deg_inv,H)
        H = H.t()
        return H

    def normalization(self, H):
        for i in range(self.num_channels):
            if i==0:
                H_ = self.norm(H[i,:,:], add=True).unsqueeze(0)
            else:
                H_ = torch.cat((H_,self.norm(H[i,:,:], add=True).unsqueeze(0)), dim=0)
        return H_

    def forward(self, A):
        A = A.unsqueeze(0).permute(0,3,1,2)
        # import pdb; pdb.set_trace()
        Ws = []
        for i in range(self.num_layers):
            if i == 0:
                H, W = self.layers[i](A)
            else:
                # pdb.set_trace()
                H = self.normalization(H)
                H, W = self.layers[i](A, H)
            Ws.append(W)

        return H


class GTConv_2(nn.Module):

    def __init__(self, in_channels, out_channels):
        super(GTConv_2, self).__init__()
        self.in_channels = in_channels
        self.out_channels = out_channels
        self.weight = nn.Parameter(torch.Tensor(in_channels, 1, 1))
        # self.scale = nn.Parameter(torch.Tensor([0.1]), requires_grad=False)
        self.reset_parameters()

    def reset_parameters(self):
        nn.init.xavier_uniform_(self.weight)

    def forward(self, A):
        A = A.permute(2, 0, 1)
        A = torch.sum(F.softmax(self.weight, dim=0) * A, dim=0)

        return A
